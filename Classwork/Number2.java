package Classwork;

public class Number2 {
    public static void Number2(int number) {

        if (number >= 0 ) {
            System.out.println("Positive number: " + number);
        }else{
            System.out.println("Negative number: " + number);
        }
    }

    public static void main(String[] args) {

        Number2(-34);
    }
}
